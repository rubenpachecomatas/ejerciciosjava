/*
 * Muestra los números múltiplos de 5 entre 0 y 100 con un bucle While.
 * 
 * @author Rubén Pacheco Matas
 */


public class Ejercicio2Bucles {
	
	public static void main (String[] args) {
		
    int x = 0;
    
    while (x < 100) {
      x = x + 5;
      System.out.println(x + " ");
    }
    
	}
}

