/*
 * Muestra los números múltiplos de 5 entre 0 y 100 con un bucle Do-While.
 * 
 * @author Rubén Pacheco Matas
 */


public class Ejercicio3Bucles {
	
	public static void main (String[] args) {
		
    int x = 0;
    do {
      x = x + 5;
      System.out.println(x + " ");
    }
    while (x < 100);
    
	}
}

